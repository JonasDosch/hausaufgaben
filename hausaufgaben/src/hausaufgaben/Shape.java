package hausaufgaben;

public class Shape {
	
	private double x;
	private double y;
	
	public Shape(double x2, double y2) {
		x=x2;
		y=y2;
		
	}
	

	public double getX(){
		return x;
	}
	
	public double getY() {
		return y;
	}
	
	public void setX(double i) {
		x=i;
	}
	public void setY(double j) {
		x=j;
	}

}
